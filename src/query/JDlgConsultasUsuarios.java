    private void jBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarActionPerformed
        // TODO add your handling code here:
        String ativo = "";
        if (jChbAtivo.isSelected()) {
            ativo = "S";
        } else if (jChbNaoAtivo.isSelected()) {
            ativo = "N";
        }
        List lista;
        if (!jChbAtivo.isSelected() && !jChbNaoAtivo.isSelected() && jTxtNome.getText().equals("")) {
            lista = usuariosDAO.listALL();
        } else {
            if ((jChbAtivo.isSelected() || jChbNaoAtivo.isSelected()) && !jTxtNome.getText().equals("")) {
                lista = usuariosDAO.listAtivoNome(ativo, jTxtNome.getText());
            } else {
                if (!jTxtNome.getText().equals("")) {
                    lista = usuariosDAO.listNome(jTxtNome.getText());
                } else {
                    lista = usuariosDAO.listAtivo(ativo);
                }
            }
        }
        usuariosControle.setList(lista);
    }//GEN-LAST:event_jBtnConsultarActionPerformed

