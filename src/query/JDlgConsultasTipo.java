    private void jBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarActionPerformed
        // TODO add your handling code here:

        List lista;
        if (jTxtPeso.getText().equals("") && jTxtPrecoMedio.getText().equals("")) {
            lista = tipoDAO.listALL();
        } else {
            if (!jTxtPeso.getText().equals("") && !jTxtPrecoMedio.getText().equals("")) {
                lista = tipoDAO.listPesoPrecoMedio(Util.strInt(jTxtPeso.getText()), Util.strInt(jTxtPrecoMedio.getText()));
            } else {
                if (!jTxtPeso.getText().equals("")) {
                    lista = tipoDAO.listPeso(Util.strInt(jTxtPeso.getText()));
                } else {
                    lista = tipoDAO.listPrecoMedio(Util.strInt(jTxtPrecoMedio.getText()));
                }
            }
        }
        tipoControle.setLista(lista);
    }//GEN-LAST:event_jBtnConsultarActionPerformed

